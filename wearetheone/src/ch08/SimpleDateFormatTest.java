package ch08;

import java.util.Date;

public class SimpleDateFormatTest {
    public static void main(String[] args) {
        Date now = new Date();

        SimpleDateFormatTest f1 = new SimpleDateFormatTest("yyyy.MM.dd. a HH:mm:ss");
        System.out.println(f1.format(now));
        SimpleDateFormatTest f2 = new SimpleDateFormatTest("오늘은 M월의 d번째 날");
        System.out.println(f2.format(now));
        SimpleDateFormatTest f3 = new SimpleDateFormatTest("오늘은 y년의 D번째 날");
        System.out.println(f3.format(now));
            }
}
